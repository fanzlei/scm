unsigned char* pin;//频率
unsigned char* pai;//节拍
sbit beez = P2^1;

void beep3(){
	int i,j;
	play:
	pin = music_code;
	pai = pin+1;
	beez = 0;
	while(1){
		if(pin == 0x00){
			//遇到结束符
			goto play;
		}
		if(pai == 0xff){
			//遇到休止符,等待一段时间
			delays(200000);
		}
		for(i=0;i<(*pai)*10;i++){
			
			for(j=0;j<(*pin);j++){
				beez = ~beez;
			}
		}
		pin += 2;
		pai += 2;
		
	}

}